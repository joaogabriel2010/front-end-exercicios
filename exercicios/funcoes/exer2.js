function mostrarTipo(variavel) {
  console.log("Tipo da variável:", typeof variavel);
}

// Exemplos de uso:
mostrarTipo(42);            // number
mostrarTipo("Olá Circo");   // string
mostrarTipo(true);          // boolean
mostrarTipo({ nome: "Ana" }); // object
mostrarTipo(undefined);     // undefined
